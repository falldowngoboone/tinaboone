                <p><span class="drop-cap">G</span>rowing up, our small corner of the world had its perks like a Dairy Queen every thirty miles and trees as far as the eye could see. I was born and raised in Southeast Texas where front porches were equipped with giant wooden swings made for sipping sweet tea on hot summer days, or most days if you’re familiar with the weather. We basically had three choices of climate: hot, mild or rainy. The latter came with an added bonus: hair curling humidity and monstrous mosquitoes.</p>
