                <p>My parents moved around for several years before finally settling in Nederland, home of the Bulldogs, where I spent many hours singing Janet Jackson songs into my hairbrush, choreographing dance routines to nineties pop in my bedroom mirror, crushing on various soccer players and musicians at school and occasionally writing lame “Dear Diary” entries about those experiences.</p>
                <p>I was one of those reluctant readers who’d rather be in the dance studio than sit still for long periods of time, but on an off day I’d catch myself writing poetry or secretly loving my English homework. At the time I was also in drill team, the Westernaires, doing what I loved most&mdash;dancing. Senior year I met my high school sweetheart (now my husband) in choir and spent the next seven months hounding my boss for time off so I could go on actual dates (I’d really had none up to that point, sad, I know).</p>
